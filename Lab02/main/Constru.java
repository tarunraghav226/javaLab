public class Constru{
    private int number;
    private String name;
    public Constru(){
        this.number=0;
        this.name="/u0000";
    }

    public String getName() {
        return name;
    }

    public int getNumber() {
        return number;
    }
    
    public void setName(String name) {
        this.name = name;
    }

    public void setNumber(int number) {
        this.number = number;
    }
}